    private void prepareForm() {
      UserProfile userProfile = ApplicationSession.getInstance().getLoginUser();
        this.lblUser.setText("Prijavljeni ste kao:" + userProfile.getEmployee().getFirstName()+", " + userProfile.getEmployee().getLastName());
        
        //ako korisnik ima ulogu administaror prikazati mu meni gradovi i meni drzave
        if (userProfile.isAdmin()){
            cityMenu.setEnabled(true);
            countryMenu.setEnabled(true);
        }else{
            cityMenu.setEnabled(false);
            countryMenu.setEnabled(false);
        }
        Hotel hotel=hotelService.find();
        txtName.setText(hotel.getName());
        txtCity.setText(hotel.getCity().toString());
        txtAddress.setText(hotel.getAddress());
        txtEmail.setText(hotel.getEmail());
        txtPhoneNo.setText(hotel.getPhoneNumber());        
           }

